<h2>Grid</h2>

<p>Snap to grid is enabled by default. To disable, choose <var>Grid</var> &rarr; <var>Snap to Grid</var> from the chart's menu.</p>

<p>The grid is 10 pixels by default, and can be any size. To change the grid size, choose <var>Grid</var> &rarr; <var>Edit Grid Size</var> from the chart's menu.</p>

<p><strong>Grid options are not saved.</strong></p>