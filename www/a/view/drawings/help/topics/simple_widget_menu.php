<h2>Line and Arrow Menu</h2>

<dl>
<dt><var>Color</var> menu</dt>
<dd>Sets the color of the line or arrow.</dd>

<dt><var>Duplicate</var></dt>
<dd>Makes a copy of the line or arrow. Offsets new duplicate below original.</dd>

<dt><var>Delete</var></dt>
<dd>Permanently removes the line or arrow, after confirmation.</dd>
</dl>