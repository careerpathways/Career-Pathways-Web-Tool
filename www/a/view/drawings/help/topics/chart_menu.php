<h2>Tools Menu</h2>

<dl>
<dt><var>New</var> menu</dt>
<dd>Creates a new box, line, or arrow on the spot clicked.</dd>

<dt><var>Grid</var> &rarr; <var>Show Grid</var></dt>
<dd>Turn the snap to grid feature on or off. Default is on.<br/>
Objects snap to nearest horizontal/vertical grid axis.
</dd>

<dt><var>Grid</var> &rarr; <var>Snap to Grid</var></dt>
<dd>Turn the snap to grid feature on or off.</dd>

<dt><va>Grid</var> &rarr; <var>Edit Grid Size</var></dt>
<dd>Opens a editor for the size of the grid layout.</dd>
</dl>