<h2>Box Menu</h2>

<dl>
<dt><var>Edit</var> &rarr; <var>Title</var></dt>
<dd>Enables an in-place text editor for the box's title.</dd>

<dt><var>Edit</var> &rarr; <var>Content</var></dt>
<dd>Opens an editor for box's content.</dd>

<dt><var>Color</var> menu</dt>
<dd>Sets the color of the box.</dd>
<?php /*
<dt><var>Box Type</var> menu</dt>
