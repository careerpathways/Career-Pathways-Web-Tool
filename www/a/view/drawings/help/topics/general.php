<h2>General</h2>

<p>Right-click the drawing area tools menu to show and/or edit the grid, and/or create new objects.</p>

<p>Right-click an object for a menu of customizing options.</p>

<p>Change an object's color by selecting it and clicking a color from <var>TOOLS</var>, or using <var>Color</var> on the menu.</p>

<p>Delete the selected object with the <kbd>DELETE</kbd> key or the object's menu.</p>

<p>Duplicate the selected object with <kbd>CTRL+C</kbd>, <kbd>CTRL+V</kbd>, or the object's menu. Offsets new duplicate below original.</p>


<h2>Positioning Connections, Lines and Arrows</h2>

<p>When selected, Connections, Lines and Arrows now show a blue box frame, with a Green start point and Red end point. The objectâ€™s menu can be brought up anywhere within this box. Click and drag the Green start point or Red end point to adjust the starting or ending location.</p>


<p>Resizing Boxes</p>

<p>When selected, boxes are highlighted with a blue box frame, with LEFT and RIGHT blue adjustment points. Click and drag either point to expand/retract box width. Box height is determined by content.</p>


<p>Print This Version</p>

<p>Click print this version from <var>TOOLS</var> to render a drawing properly for printing from the browser menu.</p>
