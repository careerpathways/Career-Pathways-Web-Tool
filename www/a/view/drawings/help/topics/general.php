<h2>General</h2>

<p>Right-click an object for a menu of customizing options.</p>

<p>Change an object's color by dragging a color onto it or using <var>Color</var> on the menu.</p>

<p>Delete an object with the <kbd>DELETE</kbd> key or the object's menu.</p>

<p>Duplicate an object with <kbd>CTRL+C</kbd>, <kbd>CTRL+V</kbd>, or the object's menu.</p>