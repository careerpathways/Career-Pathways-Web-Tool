<h2>General</h2>

<p>Right-click the chart to edit the grid or create a new object.</p>

<p>Right-click an object for a menu of customizing options.</p>

<p>Change an object's color by selecting it and clicking a color or using <var>Color</var> on the menu.</p>

<p>Delete the selected object with the <kbd>DELETE</kbd> key or the object's menu.</p>

<p>Duplicate the selected object with <kbd>CTRL+C</kbd>, <kbd>CTRL+V</kbd>, or the object's menu.</p>