<h2>Connection Menu</h2>

<dl>
<dt><var>Start Point</var> menu</dt>
<dd>Sets the side of the box the line is drawn from: <var>Top</var>, <var>Bottom</var>, <var>Left</var> or <var>Right</var>.</dd>

<dt><var>End Point</var> menu</dt>
<dd>Sets the side of the box the line is drawn to: <var>Top</var>, <var>Bottom</var>, <var>Left</var> or <var>Right</var>.</dd>

<dt><var>Orientation</var> menu</dt>
<dd>Sets the orientation (horizontal or vertical) of the line from the start point. This option is not available if segment is set to <var>1 (Direct Line)</var>.</dd>

<dt><var>Segments</var> menu</dt>
<dd>Sets the number of segments in the connecting line: <var>1 (Direct Line)</var>, <var>1 (Straight Line),</var>, <var>2</var>, or <var>3</var>.<br/>
	
	The default, one segment line, may not be connected to the end point&#8212;fix by selecting a different option.<br/>
	
	<var>1 (Direct Line)</var> creates a direct/diagonal line to the destination box regardless of box position.
</dd>

<dt><var>Color</var> menu</dt>
<dd>Sets the color of the connection.</dd>

<dt><var>Auto Position</var></dt>
<dd>Resets connection to <var>1 (Straight Line)</var>, non-direct, segment to destination box.</dd>

<dt><var>Delete</var></dt>
<dd>Permanently removes the connection, after confirmation.</dd>
</dl>